static void PeriodicProcess(void);

void MainProcess(void)
{
	/* Note: This project is overclocked to 200 MHz automatically. */
	
	LCD_Init(&blcd, &LCD_Font_1608, NULL, WHITE, BLACK);
	LCD_Print(&blcd, 0, 16 * 0, "FFT f32");
	
	SET_BIT(CoreDebug->DEMCR, CoreDebug_DEMCR_TRCENA_Msk);
	CLEAR_REG(DWT->CYCCNT);                       __DSB();
	SET_BIT(DWT->CTRL, DWT_CTRL_CYCCNTENA_Msk);   __DSB();
	
	// Size = 5244 bytes, Time = 83405 ticks
	BSP_FFT_f32(fft_src, fft_dst);
	
	__DSB(); CLEAR_BIT(DWT->CTRL, DWT_CTRL_CYCCNTENA_Msk);
	LCD_Print(&blcd, 0, 16 * 1, "CYCCNT: %u", DWT->CYCCNT);
	
	for (uint16_t i = 0; i < FFT_CALC_SIZE / 2; ++i)
		BSP_UART_Transmit_DMA(&buart, "[%03u]: %7.5f, %7.5f\n", i, fft_dst[i * 2], fft_dst[i * 2 + 1]);
	
	BSP_UART_ReceiveToIdle_DMA(&buart);
	HAL_TIM_Base_Start_IT(&htim17);
	
	while (1)
	{
		
	}
}

static void PeriodicProcess(void)
{
	uint8_t keyStatus = KEY_Read();
	
	static uint32_t timeCnt = 0;
	if (++timeCnt < 10) return;
	timeCnt = 0;
	
	LED_TG(R);
}

void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t Size)
{
	if (huart == buart.huart)
	{
		BSP_UART_Transmit_DMA(&buart, "RxLen: %u, RxMsg: %s", Size, buart.pRxMsg);
		BSP_UART_ReceiveToIdle_DMA(&buart);
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if (htim == &htim17)
	{
		PeriodicProcess();
	}
}
